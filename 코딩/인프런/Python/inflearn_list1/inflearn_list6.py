# 리스트 기초 연산
# 1. 요소 삽입
li1 = ['tv', '오디오', 'pc', '키보드']
print('li1의 주소: ', id(li1))
print(li1)

# append(추가 요소): 리스트의 마지막에 요소 삽입
print('---------------------------------------------------------------')
li1.append('마우스')
print('li1의 주소: ', id(li1))
print(li1)

# insert(인덱스, 추가 요소): 리스트의 특정 위치에 요소 삽입. 뒤 요소들이 한 칸씩 밀려남.
print('---------------------------------------------------------------')
li1.insert(1, '휴대폰')
print('li1의 주소: ', id(li1))
print(li1)

# 리스트에서 요소 찾기
# in, not in 연산자들은 리스트에 요소 존재하는지 확인.
print('---------------------------------------------------------------')
hero = ['스파이더맨', '배트맨', '슈퍼맨', '아이언맨', '헐크', '태권V']
if '배트맨' in hero:
    print('베트맨이 존재합니다.')
else:
    print('배트맨이 존재하지 않습니다.')

# index(): 리스트 내 요소의 인덱스 탐색.
# 아래처럼 list에 해당값이 없다면 에러 발생
# in, not in 연산자 통해 값의 존재 여부 체크 먼저.
if '홍당무' in hero:
    index = hero.index('홍당무')
    print('홍당무의 인덱스: ', index)
else:
    print("홍당무가 존재하지 않습니다.")

# 리스트에서 요소 삭제.
print('---------------------------------------------------------------')
# 1. 리스트명.pop(인덱스): 리스트에서 인덱스에 해당하는 요소 삭제 후, 요소 반환.
hero = ['스파이더맨', '배트맨', '슈퍼맨', '아이언맨', '헐크', '태권V', '배트맨']
element = hero.pop(0)
print("element: ", element)
print(hero)

# 2. 리스트명.remove(요소명): 리스트에서 요소명에 해당하는 요소 삭제 후, 요소 반환X.
# 리스트에 중복 요소 존재시 앞쪽 인덱스의 요소 제거
print('---------------------------------------------------------------')
hero = ['스파이더맨', '배트맨', '슈퍼맨', '아이언맨', '헐크', '태권V', '배트맨']
element = hero.remove('배트맨') 
print("element: ", element) # 반환값이 없다.
print(hero)

