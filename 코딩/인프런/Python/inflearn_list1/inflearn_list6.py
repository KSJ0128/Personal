# 중복된 요소 한 번에 삭제하는 반복문.
print('---------------------------------------------------------------')
hero = ['스파이더맨', '배트맨', '슈퍼맨', '아이언맨', '헐크', '태권V', '배트맨']
value = '배트맨'
while value in hero:
        hero.remove(value)
print(hero)

# 3. del 리스트명[인덱스]: 리스트에서 인덱스에 해당하는 요소 삭제 후, 요소 반환X
print('---------------------------------------------------------------')
hero = ['스파이더맨', '배트맨', '슈퍼맨', '아이언맨', '헐크', '태권V', '배트맨']
del hero[1] # 요소 반환 없이 단순 삭제 -> [:]로 리스트 요소 전체 삭제 가능.
print(hero)

hero = ['스파이더맨', '배트맨', '슈퍼맨', '아이언맨', '헐크', '태권V', '배트맨']
hero.clear() # 리스트 요소 전체 삭제
print(hero)

# count(): 리스트에 포함된 요소의 개수 반환
print('---------------------------------------------------------------')
hero = ['스파이더맨', '배트맨', '슈퍼맨', '아이언맨', '헐크', '태권V', '배트맨']
cnt = hero.count('배트맨') # 인자값이 필요함, 특정 요소의 값
print(cnt)

# extend(list 타입의 매개변수): 리스트를 더하는 함수, +=와 동일
print('---------------------------------------------------------------')
li1 = [1,2,3]
li2 = [10.1, 20.2]
print(li1 + li2)

#li1 += li2
li1.extend(li2)
print(li1)
